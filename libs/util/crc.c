uint32_t crc32_v2(uint32_t crc, uint8_t* data, size_t length){
  while (length--) crc = crc32_table[(crc ^ *data++) & 0xFF] ^ (crc >> 8);
  return crc;
}

uint16_t crc16_xmomdem(uint16_t crc, uint8_t* data, size_t length){
  while(length--) crc = ((crc << 8) & 0xff00) ^ crc16_table[((crc >> 8) & 0xff) ^ (*data++)];
  return crc;
}
